export const Regex = {
  /**
   * 휴대폰 번호 (010-1234-5678 or 01012345678)
   * 가운데 자리는 3~4자리 허용
   */
  PHONE: /^01[0-9]-?[0-9]{3,4}-?[0-9]{4}$/,

  /**
   * 비밀번호: 영문자 포함
   */
  PASSWORD_ALPHA: /[A-Za-z]/,

  /**
   * 비밀번호: 숫자 포함
   */
  PASSWORD_NUMBER: /[0-9]/,

  /**
   * 시간 형식 (HH:MM) 24시간제
   */
  TIME_HHMM: /^([01]\d|2[0-3]):([0-5]\d)$/,
} as const;
